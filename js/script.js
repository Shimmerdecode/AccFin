let answers = new Array(105).fill(null);
let currentPage = 1;
const questionsPerPage = 10;
const totalPages = Math.ceil(questions.length / questionsPerPage);

function loadAnswers() {
    const saved = localStorage.getItem('test_answers_full');
    if (saved) {
        const parsed = JSON.parse(saved);
        if (parsed && parsed.length === 105) answers = parsed;
    }
}

function saveAnswers() {
    localStorage.setItem('test_answers_full', JSON.stringify(answers));
}

function getGroupFromURL() {
    const params = new URLSearchParams(window.location.search);
    if (params.has('group')) return params.get('group');
    return "default";
}

function renderPage() {
    const start = (currentPage - 1) * questionsPerPage;
    const end = Math.min(start + questionsPerPage, questions.length);
    let html = '';
    for (let i = start; i < end; i++) {
        const qNum = i + 1;
        const currentAnswer = answers[qNum];
        html += '<div class="question-card"><div class="question-text"><strong>' + qNum + '.</strong> ' + questions[i] + '</div><div class="options" data-qnum="' + qNum + '">';
        const vals = [-2, -1, 0, 1, 2];
        for (let v = 0; v < vals.length; v++) {
            const val = vals[v];
            let labelText = '';
            if (val === -2) labelText = 'Нет';
            else if (val === -1) labelText = 'Скорее нет';
            else if (val === 0) labelText = 'Не знаю';
            else if (val === 1) labelText = 'Скорее да';
            else if (val === 2) labelText = 'Да';
            const checked = (currentAnswer === val) ? 'checked' : '';
            html += '<label class="option ' + (currentAnswer === val ? 'selected' : '') + '"><input type="radio" name="q' + qNum + '" value="' + val + '" ' + checked + '> <span style="font-size:14px">' + labelText + '</span></label>';
        }
        html += '</div></div>';
    }
    document.getElementById('questionsContainer').innerHTML = html;
    
    document.querySelectorAll('.options').forEach(function(container) {
        const qNum = parseInt(container.dataset.qnum);
        const radios = container.querySelectorAll('input');
        for (let r = 0; r < radios.length; r++) {
            radios[r].addEventListener('change', function(e) {
                answers[qNum] = parseInt(e.target.value);
                saveAnswers();
                updateProgress();
                const card = container.closest('.question-card');
                if (card) {
                    card.style.backgroundColor = '#f0fdf4';
                    setTimeout(function() { card.style.backgroundColor = ''; }, 300);
                }
            });
        }
    });
    
    document.getElementById('pageIndicator').textContent = 'Страница ' + currentPage + ' / ' + totalPages;
    document.getElementById('prevBtn').disabled = (currentPage === 1);
    const nextBtn = document.getElementById('nextBtn');
    nextBtn.textContent = (currentPage === totalPages) ? 'Завершить' : 'Далее';
    updateProgress();
}

function updateProgress() {
    let answered = 0;
    for (let i = 1; i <= 104; i++) {
        if (answers[i] !== null) answered++;
    }
    const percent = Math.round((answered / 104) * 100);
    const progressFill = document.getElementById('progressFill');
    const progressPercent = document.getElementById('progressPercent');
    if (progressFill) progressFill.style.width = percent + '%';
    if (progressPercent) progressPercent.textContent = percent + '%';
}

function calculateScores() {
    let scores = new Array(13).fill(0);
    for (let i = 1; i <= 104; i++) {
        const ans = answers[i];
        if (ans !== null) {
            const scaleIdx = questionToScale[i];
            if (scaleIdx !== undefined) scores[scaleIdx] += ans;
        }
    }
    return scores;
}

function getLevel(score) {
    if (score >= 11) return "доминирующее качество";
    if (score >= 6) return "проявляется в достаточной степени";
    if (score >= 1) return "проявляется периодически";
    if (score >= -4) return "проявляется слабо";
    if (score >= -9) return "проявляется очень слабо";
    return "качество не свойственно";
}

function generateFullReport(scores, userName) {
    // Собираем все шкалы с баллами
    let allScales = [];
    for (let i = 0; i < 13; i++) {
        allScales.push({
            tech: scaleTechnical[i],
            name: scaleUserNames[i],
            short: scaleShortNames[i],
            score: scores[i],
            description: strongDescriptions[i],
            leadershipAdvice: leadershipAdvice[i],
            reduceAdvice: reduceAdvice[i]
        });
    }
    
    // 1. Сильные стороны (только лидерские шкалы 0-4, балл >= 6)
    let strongScales = allScales.slice(0,5).filter(s => s.score >= 6);
    strongScales.sort((a, b) => b.score - a.score);
    
    // 2. Что развивать в лидере (лидерские шкалы 0-4, балл <= 6)
    let developLeadership = allScales.slice(0,5).filter(s => s.score <= 6);
    
    // 3. Что снизить (исполнительские шкалы 5-11, балл > 6)
    let reduceIndices = [5, 6, 7, 8, 9, 10, 11];
    let reduceScales = [];
    for (let i = 0; i < reduceIndices.length; i++) {
        let idx = reduceIndices[i];
        if (allScales[idx] && allScales[idx].score > 6) {
            reduceScales.push(allScales[idx]);
        }
    }
    
    // Формируем HTML
    let html = '<h2>' + userName + ', ваш результат</h2>';
    
    // Блок 1: Сильные стороны
    if (strongScales.length > 0) {
        html += '<div class="result-section"><h3>Ваши сильные стороны</h3>';
        for (let s of strongScales) {
            html += '<div class="result-item">';
            html += '<div class="result-name"><strong>' + s.short + '</strong></div>';
            html += '<div class="result-desc">' + s.description + '</div>';
            html += '</div>';
        }
        html += '</div>';
    }
    
    // Блок 2: Что развивать в лидере
    if (developLeadership.length > 0) {
        html += '<div class="result-section"><h3>Что развивать в себе как в лидере</h3>';
        for (let s of developLeadership) {
            html += '<div class="result-item">';
            html += '<div class="result-name"><strong>' + s.short + '</strong></div>';
            html += '<div class="result-desc">Как развивать: ' + s.leadershipAdvice + '</div>';
            html += '</div>';
        }
        html += '</div>';
    }
    
    // Блок 3: Что снизить
    if (reduceScales.length > 0) {
        html += '<div class="result-section"><h3>Что стоит снизить (балансировка)</h3>';
        for (let s of reduceScales) {
            html += '<div class="result-item">';
            html += '<div class="result-name"><strong>' + s.short + '</strong></div>';
            html += '<div class="result-desc">Рекомендация: ' + s.reduceAdvice + '</div>';
            html += '</div>';
        }
        html += '</div>';
    }
    
    // Блок 4: Профессии (структурированно по шкалам, где балл > 6)
    let allHighScales = allScales.filter(s => s.score > 6);
    
    if (allHighScales.length > 0) {
        html += '<div class="result-section"><h3>Рекомендуемые профессии</h3>';
        
        for (let s of allHighScales) {
            if (scaleProfessions[s.short] && scaleProfessions[s.short].length > 0) {
                html += '<div class="result-item">';
                html += '<div class="result-name"><strong>' + s.short + '</strong></div>';
                html += '<div class="result-desc">';
                for (let prof of scaleProfessions[s.short]) {
                    html += '• ' + prof + '<br>';
                }
                html += '</div></div>';
            }
        }
        html += '</div>';
    } else {
        html += '<div class="result-section"><h3>Рекомендуемые профессии</h3>';
        html += '<div class="result-item"><div class="result-desc">Скоро здесь появятся профессии, подходящие вашему профилю.</div></div>';
        html += '</div>';
    }
    
    // Кнопка "Пройти заново"
    html += '<button onclick="location.reload()" style="margin-top:20px; padding:10px 24px; background:#667eea; color:white; border:none; border-radius:40px; cursor:pointer;">Пройти заново</button>';
    
    return html;
}

function sendToSheets() {
    const nameInput = document.getElementById('userName').value.trim();
    const finalName = (nameInput === "") ? "Аноним" : nameInput;
    const scores = calculateScores();
    const payload = {
        timestamp: new Date().toISOString(),
        name: finalName,
        group: getGroupFromURL(),
        scores: scores
    };
    console.log("Отправляю:", payload);
    
    fetch(GOOGLE_SHEETS_WEBAPP_URL, {
        method: 'POST',
        mode: 'no-cors',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(payload)
    }).catch(function(e) { console.error("Ошибка:", e); });
}

function finishTest() {
    let answered = 0;
    for (let i = 1; i <= 104; i++) {
        if (answers[i] !== null) answered++;
    }
    if (answered < 104) {
        alert('Вы ответили не на все вопросы. Ответьте на все 104 вопроса.');
        return;
    }
    const scores = calculateScores();
    const userName = document.getElementById('userName').value.trim() || "Аноним";
    const reportHtml = generateFullReport(scores, userName);
    document.getElementById('resultContent').innerHTML = reportHtml;
    document.getElementById('resultModal').style.display = 'flex';
    sendToSheets();
}

function nextPage() {
    if (currentPage < totalPages) {
        currentPage++;
        renderPage();
        window.scrollTo({ top: 0 });
    } else {
        finishTest();
    }
}

function prevPage() {
    if (currentPage > 1) {
        currentPage--;
        renderPage();
        window.scrollTo({ top: 0 });
    }
}

function init() {
    loadAnswers();
    renderPage();
    document.getElementById('prevBtn').onclick = prevPage;
    document.getElementById('nextBtn').onclick = nextPage;
    document.querySelector('.modal-close').onclick = function() {
        document.getElementById('resultModal').style.display = 'none';
    };
    window.onclick = function(e) {
        if (e.target === document.getElementById('resultModal')) {
            document.getElementById('resultModal').style.display = 'none';
        }
    };
    setInterval(function() { saveAnswers(); }, 5000);
}

